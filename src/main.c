void swap(int* a, int* b){
    int temp = *a;
    *a = *b;
    *b = temp;
}

void drawLine(int x0, int y0, int x1, int y1, short int color){
    bool steep = abs(y1-y0) > abs(x1-x0);
    if(steep){
        swap(&x0 , &y0);
        swap(&x1,&y1);
    }
    if(x0>x1){
        swap(&x0,&x1);
        swap(&y0,&y1);
    }

    int delx = x1 - x0;
    int dely = abs(y1-y0);
    int error = -delx/2;
    int y = y0;
    int ystep;

    if(y0<y1){
       ystep = 1; 
    }
    else{
        ystep = -1;
    }

    for(int x = x0; x <= x1; x++){
        if(steep){
            plotPixel(y,x,color);
        }
        else{
            plotPixel(x,y,color);
        }
        error = error + dely;
        if(error >= 0){
            y += ystep;
            error -= delx;
        }
    }
}

void waitForSync(){
    volatile int *pixel_ctrl_ptr = (int*) 0xFF203020;
    register int status;

    *pixel_ctrl_ptr = 1;

    status = *(pixel_ctrl_ptr+3);
    while((status & 0x01) != 0){
        status = *(pixel_ctrl_ptr+3);
    }
}

